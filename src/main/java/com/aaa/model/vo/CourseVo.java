public class CourseVo {
	private String weeks;
	private String coursecode;
	private String coursename;
	private String zcoursecode;
	private String stage;
	private String zcoursecotent;
	private String coursepoints;
	
	
	public String getWeeks() {
		return weeks;
	}
	public void setWeeks(String weeks) {
		this.weeks = weeks;
	}
	public String getCoursecode() {
		return coursecode;
	}
	public void setCoursecode(String coursecode) {
		this.coursecode = coursecode;
	}
	public String getCoursename() {
		return coursename;
	}
	public void setCoursename(String coursename) {
		this.coursename = coursename;
	}
	public String getZcoursecode() {
		return zcoursecode;
	}
	public void setZcoursecode(String zcoursecode) {
		this.zcoursecode = zcoursecode;
	}
	
	public String getStage() {
		return stage;
	}
	public void setStage(String stage) {
		this.stage = stage;
	}
	public String getZcoursecotent() {
		return zcoursecotent;
	}
	public void setZcoursecotent(String zcoursecotent) {
		this.zcoursecotent = zcoursecotent;
	}
	public String getCoursepoints() {
		return coursepoints;
	}
	public void setCoursepoints(String coursepoints) {
		this.coursepoints = coursepoints;
	}
	
	
}
